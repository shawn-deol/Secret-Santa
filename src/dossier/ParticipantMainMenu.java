//Saves the information inputted into the java textfields
private void saveInformationActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveInformationActionPerformed
    //Case and switch to determine value of combo box selection
    switch (grade.getSelectedIndex()) {
        case 0:
            gradeNumber = 9;
            break;
        case 1:
            gradeNumber = 10;
            break;
        case 2:
            gradeNumber = 11;
            break;
        case 3:
            gradeNumber = 12;
            break;
    }
    //Sets all the values of the users participant object which is fetched from the login screen
    LogIn.q.setGrade(gradeNumber);
    LogIn.q.setFirstParticipantPreference(firstPreference.getText());
    LogIn.q.setSecondParticipantPreference(secondPreference.getText());
    LogIn.q.setThirdParticipantPreference(thirdPreference.getText());
    secretSantaStore.writeParticpantToFile();
}//GEN-LAST:event_saveInformationActionPerformed


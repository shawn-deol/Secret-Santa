//This button loads all the information of a user and makes the text uneditable//This button sets a new password
private void saveNewPasswordActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveNewPasswordActionPerformed
    //Error check boolean
    Boolean errorCheck = true;
    //Checks if actual password and oldPassword are equal
    if (oldPassword.getText().compareTo(LogIn.q.password) != 0) {
        errorCheck = false;
        JOptionPane.showMessageDialog(this, "Incorrect password",
                "Error", JOptionPane.ERROR_MESSAGE);
        errorCheck = false;
    } //Checks if newPassword and confirmPassword are equal
    else if (newPassword.getText().compareTo(confirmPassword.getText()) != 0) {
        errorCheck = false;
        JOptionPane.showMessageDialog(this, "New password and confirmed password are different",
                "Error", JOptionPane.ERROR_MESSAGE);
        errorCheck = false;
        //Checks if password fields were left blank
    } else if (oldPassword.getText().equals("") || newPassword.getText().equals("") || confirmPassword.getText().equals("")) {
        JOptionPane.showMessageDialog(this, "You missed a spot",
                "Error", JOptionPane.ERROR_MESSAGE);
        errorCheck = false;
    } else {
        LogIn.q.setPassword(newPassword.getText());
        secretSantaStore.writeParticpantToFile();
    }

}//GEN-LAST:event_saveNewPasswordActionPerformed

private void searchParticipantActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchParticipantActionPerformed
    //Sets index to passed by findBy method
    int indexOfSearchedParticipant = secretSantaStore.findParticipantByName(participantFirstNameSearch.getText().trim(), participantLastNameSearch.getText().trim());
   //If the index is real then set all of the values to the participant with that index
    if (indexOfSearchedParticipant > -1) {
        firstName.setText(secretSantaStore.participantsList[indexOfSearchedParticipant].getFirstName());
        lastName.setText(secretSantaStore.participantsList[indexOfSearchedParticipant].getlastName());
        participantGrade.setText(Integer.toString(secretSantaStore.participantsList[indexOfSearchedParticipant].getGrade()));
        username.setText(secretSantaStore.participantsList[indexOfSearchedParticipant].getUsername());
        password.setText(secretSantaStore.participantsList[indexOfSearchedParticipant].getPassword());
    } //If the index is not real display error message
    else {
        JOptionPane.showMessageDialog(this, "User could not be found",
                "Error", JOptionPane.ERROR_MESSAGE);
    }
}//GEN-LAST:event_searchParticipantActionPerformed

private void searchEventActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchEventActionPerformed
    //Sets index to value retrieved from findBy
    int indexOfSearchedEvent = secretSantaStore.findEventByTitle(eventTitleSearch.getText().trim(), eventAuthorSearch.getText().trim());
   //If Index is real then set all the values to those of object at the above index
    if (indexOfSearchedEvent > -1) {
        title.setText(secretSantaStore.eventsListArray[indexOfSearchedEvent].getTitle());
        author.setText(secretSantaStore.eventsListArray[indexOfSearchedEvent].getAuthor());
        grade.setText(Integer.toString(secretSantaStore.eventsListArray[indexOfSearchedEvent].getGrade()));
        size.setText(Integer.toString(secretSantaStore.eventsListArray[indexOfSearchedEvent].getEventSize()));
    }//Else display error message
    else {
        JOptionPane.showMessageDialog(this, "Event could not be found",
                "Error", JOptionPane.ERROR_MESSAGE);
    }
}//GEN-LAST:event_searchEventActionPerformed

    public void eventDropDownList() {
        //Set maximum row count to number of participants
        String[] x = new String[secretSantaStore.numberOfEventsInStore];
        //Assign rows in terms of items to title of events located in in the event store
        for (int i = 0; i < x.length; i++) {
            x[i] = secretSantaStore.eventsListArray[i].getTitle();
        }
        eventsListComboBox.setModel(new javax.swing.DefaultComboBoxModel(x));
    }
